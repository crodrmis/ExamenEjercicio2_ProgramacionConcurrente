package com.project.accesoparque;

public class Visitante {
    private final int id;
    private final int codigodeAcceso;

    public Visitante(int id, int codigoAcceso) {
        this.id = id;
        this.codigodeAcceso = codigodeAcceso;
        this.codigodeAcceso = codigoAcceso;
    }

    public int getId() {
        return id;
    }

    public int getCodigoAcceso() {
        return codigodeAcceso;
    }
}
